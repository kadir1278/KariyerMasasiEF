        public override string[] GetRolesForUser(string username)
        {
            SystemContext db = new SystemContext();
            var loginUser = db.UserRoles.Include(x=>x.User).Include(x=>x.Role).Where(x => !x.DeletionStatus && x.User.EMail == username).FirstOrDefault();
